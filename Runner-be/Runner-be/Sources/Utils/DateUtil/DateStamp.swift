import Foundation

enum DateStamp {
    static let MINUTE: Double = 60.0
    static let HOUR: Double = 3600.0
    static let DAY: Double = 86400.0
    static let WEEK: Double = 604_800.0
    static let MONTH: Double = 2_629_743.0
    static let YEAR: Double = 31_556_926.0
}
